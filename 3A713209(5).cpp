int main(int argc, char *argv[]) {
  int h;
  int m;
  int s;
  printf("輸入小時");
  scanf("%d",&h);
  printf("輸入分鐘");
  scanf("%d",&m);
  printf("輸入秒");
  scanf("%d",&s);
  printf("%d時:%d分:%d秒\n",h,m,s); 
  int h2;
  int m2;
  int s2;
  printf("輸入小時");
  scanf("%d",&h2);
  printf("輸入分鐘");
  scanf("%d",&m2);
  printf("輸入秒");
  scanf("%d",&s2);
  printf("%d時:%d分:%d秒\n",h2,m2,s2);
  int hh = (h - h2) * 3600;
  int mm = (m - m2) * 60 ;
  int ss = s - s2;
  int t = hh + mm + ss;
  printf("秒數差為:%d",t);    
  system("PAUSE"); 
  return 0;
}
